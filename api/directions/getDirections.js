import { useRuntimeConfig } from '#imports';

export default async function getDirections(opts, id) {
  const config = useRuntimeConfig();

  try {
    const headers = {
      Accept: 'application/vnd.api+json',
    };

    const { data } = await useAsyncData(`directions-${id}`, () =>
      $fetch(`v1/organization/directions`, {
        baseURL: config.public.baseURL,
        headers,
        method: 'GET',
        params: {
          ...opts,
        },
      }),
    );

    return data.value;
  } catch {
    return [];
  }
}
